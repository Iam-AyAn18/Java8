public class AbstractInherit extends AbstractClass{

	@Override
	void getReady() {
		System.out.println("getReady");
		
	}

	@Override
	void doTheDish() {
		System.out.println("doTheDish");
		
	}

	@Override
	void cleanUp() {
		System.out.println("cleanUp");
		
	}

}
